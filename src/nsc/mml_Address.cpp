mml_Address::~mml_Address(void)
{
}

//==============================================================
//		アドレスの設定
//--------------------------------------------------------------
//	●引数
//				無し
//	●返値
//				無し
//==============================================================
void	mml_Address::set_Address(unsigned int _addr)
{
	switch(iSize){
		case(3):
			code[1] = ((_addr     ) & 0xFF);
			code[2] = ((_addr >> 8) & 0xFF);
			break;
		case(4):
			code[2] = ((_addr     ) & 0xFF);
			code[3] = ((_addr >> 8) & 0xFF);
			break;
		default:
			wcout << L"mml_Address::set_Address()関数でエラーが発生しました。" << endl;
			exit(-1);
			break;
	}
	

}

